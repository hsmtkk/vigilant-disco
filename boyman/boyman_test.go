package boyman_test

import (
	"testing"

	"github.com/hsmtkk/vigilant-disco/boyman"
	"github.com/stretchr/testify/assert"
)

type testCase struct {
	input []int
	want  []int
}

func TestMenFromBoys(t *testing.T) {
	testCases := []testCase{
		{[]int{7, 3, 14, 17}, []int{14, 17, 7, 3}},
		{[]int{2, 43, 95, 90, 37}, []int{2, 90, 95, 43, 37}},
		{[]int{20, 33, 50, 34, 43, 46}, []int{20, 34, 46, 50, 43, 33}},
		{[]int{82, 91, 72, 76, 76, 100, 85}, []int{72, 76, 82, 100, 91, 85}},
		{[]int{2, 15, 17, 15, 2, 10, 10, 17, 1, 1}, []int{2, 10, 17, 15, 1}},
		{[]int{-32, -39, -35, -41}, []int{-32, -35, -39, -41}},
		{[]int{-64, -71, -63, -66, -65}, []int{-66, -64, -63, -65, -71}},
		{[]int{-94, -99, -100, -99, -96, -99}, []int{-100, -96, -94, -99}},
		{[]int{-53, -26, -53, -27, -49, -51, -14}, []int{-26, -14, -27, -49, -51, -53}},
		{[]int{-17, -45, -15, -33, -85, -56, -86, -30}, []int{-86, -56, -30, -15, -17, -33, -45, -85}},
		{[]int{12, 89, -38, -78}, []int{-78, -38, 12, 89}},
		{[]int{2, -43, 95, -90, 37}, []int{-90, 2, 95, 37, -43}},
		{[]int{82, -61, -87, -12, 21, 1}, []int{-12, 82, 21, 1, -61, -87}},
		{[]int{63, -57, 76, -85, 88, 2, -28}, []int{-28, 2, 76, 88, 63, -57, -85}},
		{[]int{49, 818, -282, 900, 928, 281, -282, -1}, []int{-282, 818, 900, 928, 281, 49, -1}},
	}
	for _, tc := range testCases {
		got := boyman.MenFromBoys(tc.input)
		assert.Equal(t, tc.want, got)
	}
}
